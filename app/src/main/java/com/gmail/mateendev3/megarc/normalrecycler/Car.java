package com.gmail.mateendev3.megarc.normalrecycler;

public class Car {
    private final String title;
    private final String price;

    public Car(String title, String price) {
        this.title = title;
        this.price = price;
    }

    public String getTitle() {
        return title;
    }

    public String getPrice() {
        return price;
    }
}
